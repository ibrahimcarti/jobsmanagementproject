    public void getJobsToTable() {
        model = (DefaultTableModel) tblJobs.getModel();
        model.setRowCount(0);
        JobsManager jobmanager = new JobsManager(new JobsDal());
       
        ArrayList<Jobs> jobs = jobmanager.getJobsById(userId);
        for (Jobs job : jobs) {
            Object[] row = {job.getId(), job.getJobtitle(), job.getDeadline(), job.getGivername(),job.getIsDone()};
            model.addRow(row);
        }
    }

