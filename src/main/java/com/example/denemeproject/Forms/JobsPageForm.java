    private void tblJobsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblJobsMouseClicked
        int selectedRow=tblJobs.getSelectedRow();
        if(selectedRow!=-1){
            Object idObject = tblJobs.getValueAt(selectedRow, 0);
            Object jobTitleObject = tblJobs.getValueAt(selectedRow, 1);
            Object deadlineObject = tblJobs.getValueAt(selectedRow, 2);
            txtId.setText(idObject.toString());
            txtJobTitle.setText(jobTitleObject.toString());
            txtJobView.setText(jobTitleObject.toString());
            dateTimePicker.setValue((Date)deadlineObject);
            
        }
    }//GEN-LAST:event_tblJobsMouseClicked

