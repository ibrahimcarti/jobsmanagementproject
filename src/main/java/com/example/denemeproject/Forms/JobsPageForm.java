    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        JobsManager jobmanager = new JobsManager(new JobsDal());
        Jobs job = new Jobs();
        java.util.Date utilDate = (java.util.Date) dateTimePicker.getValue();
        java.sql.Date deadline = new java.sql.Date(utilDate.getTime());
        String selectedDone = jComboBox1.getSelectedItem().toString();
        ArrayList<Jobs> jobs = jobmanager.getJobsById(userId);
        for (Jobs joblist : jobs) {
        }
        String givername = jobs.get(0).getGivername();
        int giverid = jobs.get(0).getGiverid();
        job.setId(Integer.parseInt(txtId.getText()));
        job.setJobtitle(txtJobTitle.getText());
        job.setDeadline(deadline);
        job.setGivenid(userId);
        job.setGivenname(userName);
        job.setIsDone(Integer.parseInt(selectedDone));
        job.setGiverid(giverid);
        job.setGivername(givername);
        jobmanager.update(job);
        getJobsToTable();
    }//GEN-LAST:event_btnUpdateActionPerformed

