    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        String givenname = jComboBox1.getSelectedItem().toString();
        UsersManager usermanager = new UsersManager(new UsersDal());
        String username = usermanager.getUsernameById(userid);
        java.util.Date utilDate = (java.util.Date) dateTimePicker.getValue();
        java.sql.Date deadline = new java.sql.Date(utilDate.getTime());
        selectedId = usermanager.getUserIdByUsername(givenname);
        JobsManager jobmanager = new JobsManager(new JobsDal());
        Jobs job = new Jobs();
        job.setJobtitle(txtJobTitle.getText());
        job.setDeadline(deadline);
        job.setGiverid(userid);
        job.setGivername(username);
        job.setGivenid(selectedId);
        job.setGivenname(givenname);
        jobmanager.add(job);
        getJobsToTable();
    }//GEN-LAST:event_btnAddActionPerformed

