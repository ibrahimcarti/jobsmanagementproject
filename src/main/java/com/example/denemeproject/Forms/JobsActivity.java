    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        String searchText = txtSearch.getText();
        TableRowSorter<DefaultTableModel> tableRowSorter = new TableRowSorter<DefaultTableModel>(model);
        tblJobs.setRowSorter(tableRowSorter);
        tableRowSorter.setRowFilter(RowFilter.regexFilter(searchText));
    }//GEN-LAST:event_txtSearchKeyReleased

    private void tblJobsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblJobsMouseClicked
        int selectedRow=tblJobs.getSelectedRow();
        if(selectedRow!=-1){
            Object idObject = tblJobs.getValueAt(selectedRow, 0);
            Object jobTitleObject = tblJobs.getValueAt(selectedRow, 1);
            Object deadlineObject = tblJobs.getValueAt(selectedRow, 2);
            Object givenNameObject = tblJobs.getValueAt(selectedRow, 3);
            Object giverNameObject = tblJobs.getValueAt(selectedRow, 4);
            txtId.setText(idObject.toString());
            txtJobTitle.setText(jobTitleObject.toString());
            dateTimePicker.setValue((Date)deadlineObject);
            username=giverNameObject.toString();
            int index = jComboBox1.getItemCount(); // Varsayılan olarak seçili değeri boş yapalım
                    for (int i = 0; i < jComboBox1.getItemCount(); i++) {
                        if (givenNameObject.equals(jComboBox1.getItemAt(i))) {
                            index = i;
                            break;
                        }
                    }
                    jComboBox1.setSelectedIndex(index);
        }
    }//GEN-LAST:event_tblJobsMouseClicked

    /**
     * @param args the command line arguments
     */
    public void getJobsToTable() {
        model = (DefaultTableModel) tblJobs.getModel();
        model.setRowCount(0);
        JobsManager jobmanager = new JobsManager(new JobsDal());

        ArrayList<Jobs> jobs = jobmanager.getJobs();
        for (Jobs job : jobs) {
            Object[] row = {job.getId(), job.getJobtitle(), job.getDeadline(), job.getGivername(), job.getGivenname(), job.getIsDone()};
            model.addRow(row);
        }
    }

